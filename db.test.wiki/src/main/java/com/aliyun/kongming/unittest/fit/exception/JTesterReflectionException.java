package com.aliyun.kongming.unittest.fit.exception;

public class JTesterReflectionException extends RuntimeException {
    private static final long serialVersionUID = 1421565945736042586L;

    public JTesterReflectionException() {
        super();
    }

    public JTesterReflectionException(String message, Throwable cause) {
        super(message, cause);
    }

    public JTesterReflectionException(String message) {
        super(message);
    }

    public JTesterReflectionException(Throwable cause) {
        super(cause);
    }
}
